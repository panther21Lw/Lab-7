/**
 * Цей інтерфейс описує специфікацію команди. Він містить методи для виконання команди
 * та отримання ідентифікатора команди.   
 */
public interface Command {
    /**
     * Цей метод ініціює виконання команди. Реалізація цього методу повинна виконувати логіку, 
     * що відповідає за виконання конкретної команди.
     */
    void execute();
    /**
     * Цей метод повертає ідентифікатор команди.
     * @return Цілочислений ідентифікатор команди.
     */
    int getId();
}